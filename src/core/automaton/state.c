/**
 * @brief Create a state object (standalone, not added to an automaton)
 *
 * @param is_accepting Whether this is an accepting state
 * @return The newly created state or NULL on failure
 */
rift_regex_state_t *
rift_state_create(bool is_accepting)
{
    /* Allocate memory for the state */
    rift_regex_state_t *state = (rift_regex_state_t *)malloc(sizeof(rift_regex_state_t));
    if (!state) {
        return NULL;
    }

    /* Initialize the state */
    state->id = next_state_id++;
    state->is_accepting = is_accepting;
    state->pattern = NULL;
    state->transitions = NULL;
    state->num_transitions = 0;
    state->transition_capacity = 0;
    state->user_data = NULL;

    /* Initialize group data */
    state->group_name = NULL;
    state->is_group_start = false;
    state->is_group_end = false;

    /* Initialize flags */
    state->flags = RIFT_STATE_FLAG_NONE;

    return state;
}

bool
rift_automaton_state_set_accepting(rift_regex_state_t *state, bool is_accepting)
{
    if (!state)
        return false;
    state->is_accepting = is_accepting;
    return true;
}

bool
rift_automaton_set_state_flag(rift_regex_state_t *state, rift_regex_flags_t flag)
{
    if (!state)
        return false;
    state->flags |= flag;
    return true;
}

