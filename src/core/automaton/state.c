/**
 * @brief Clone a state
 *
 * This function creates a copy of a state without its transitions.
 *
 * @param state The state to clone
 * @return A new state that is a copy of the original, or NULL on failure
 */
rift_regex_state_t *
rift_state_clone(const rift_regex_state_t *state)
{
    if (!state) {
        return NULL;
    }

    /* Create a new state */
    rift_regex_state_t *clone = rift_state_create(state->is_accepting);
    if (!clone) {
        return NULL;
    }

    /* Copy pattern if it exists */
    if (state->pattern) {
        clone->pattern = strdup(state->pattern);
        if (!clone->pattern) {
            rift_state_free(clone);
            return NULL;
        }
    }

    /* Copy group name if it exists */
    if (state->group_name) {
        clone->group_name = strdup(state->group_name);
        if (!clone->group_name) {
            rift_state_free(clone);
            return NULL;
        }
    }

    /* Copy other simple properties */
    clone->is_group_start = state->is_group_start;
    clone->is_group_end = state->is_group_end;
    clone->flags = state->flags;

    /* User data is typically not cloned */

    return clone;
}

