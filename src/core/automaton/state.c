/**
 * @brief Add a transition from a state
 *
 * @param from_state The source state
 * @param to_state The target state
 * @param input_pattern The pattern that triggers this transition
 * @return true if successful, false otherwise
 */
bool
rift_state_add_transition(rift_regex_state_t *from_state, rift_regex_state_t *to_state,
                          const char *input_pattern)
{
    if (!from_state || !to_state || !input_pattern) {
        return false;
    }

    /* Create transition object */
    struct rift_regex_transition *transition =
        rift_transition_create(from_state, to_state, input_pattern);

    if (!transition) {
        return false;
    }

    /* Check if we need to resize the transitions array */
    if (from_state->num_transitions >= from_state->transition_capacity) {
        size_t new_capacity =
            from_state->transition_capacity == 0 ? 4 : from_state->transition_capacity * 2;

        struct rift_regex_transition **new_transitions = (struct rift_regex_transition **)realloc(
            from_state->transitions, sizeof(struct rift_regex_transition *) * new_capacity);

        if (!new_transitions) {
            rift_transition_free(transition);
            return false;
        }

        from_state->transitions = new_transitions;
        from_state->transition_capacity = new_capacity;
    }

    /* Add the transition to the state */
    from_state->transitions[from_state->num_transitions++] = transition;
    return true;
}

