/**
 * @brief Free a state and all its resources
 *
 * @param state The state to free
 */
void
rift_state_free(rift_regex_state_t *state)
{
    if (!state) {
        return;
    }

    /* Free the pattern */
    if (state->pattern) {
        free(state->pattern);
    }

    /* Free the group name */
    if (state->group_name) {
        free(state->group_name);
    }

    /* Free all transitions */
    if (state->transitions) {
        for (size_t i = 0; i < state->num_transitions; i++) {
            rift_transition_free(state->transitions[i]);
        }
        free(state->transitions);
    }

    /* Free the state itself */
    free(state);
}

