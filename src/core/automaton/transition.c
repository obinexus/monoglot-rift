/**
 * @brief Compare two transitions for equality
 *
 * @param t1 The first transition
 * @param t2 The second transition
 * @return true if the transitions are equal, false otherwise
 */
bool
rift_transitions_are_equal(const rift_regex_transition_t *t1, const rift_regex_transition_t *t2)
{
    if (!t1 || !t2) {
        return false;
    }

    /* Check epsilon status first */
    if (t1->is_epsilon != t2->is_epsilon) {
        return false;
    }

    /* Check if the states match */
    if (t1->from_state != t2->from_state || t1->to_state != t2->to_state) {
        return false;
    }

    /* For epsilon transitions, we've already checked what we need to */
    if (t1->is_epsilon) {
        return true;
    }

    /* For non-epsilon transitions, compare patterns */
    if (!t1->input_pattern || !t2->input_pattern) {
        return t1->input_pattern == t2->input_pattern;
    }

    return strcmp(t1->input_pattern, t2->input_pattern) == 0;
}

