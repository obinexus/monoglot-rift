/**
 * @brief Create a new transition between states
 *
 * @param from_state The source state
 * @param to_state The target state
 * @param input_pattern The pattern that triggers this transition
 * @return The newly created transition or NULL on failure
 */
rift_regex_transition_t *
rift_transition_create(rift_regex_state_t *from_state, rift_regex_state_t *to_state,
                       const char *input_pattern)
{
    if (!from_state || !to_state || !input_pattern) {
        return NULL;
    }

    /* Allocate memory for the transition */
    rift_regex_transition_t *transition =
        (rift_regex_transition_t *)malloc(sizeof(rift_regex_transition_t));
    if (!transition) {
        return NULL;
    }

    /* Initialize the transition */
    transition->from_state = from_state;
    transition->to_state = to_state;
    transition->is_epsilon = false;
    transition->priority = 0;

    /* Copy the input pattern */
    transition->input_pattern = strdup(input_pattern);
    if (!transition->input_pattern) {
        free(transition);
        return NULL;
    }

    return transition;
}

