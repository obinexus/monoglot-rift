/**
 * @brief Clone a transition
 *
 * This creates a deep copy of a transition. Note that this does not update
 * the from_state and to_state of the transitions, which must be handled separately.
 *
 * @param transition The transition to clone
 * @return A new transition that is a copy of the original, or NULL on failure
 */
rift_regex_transition_t *
rift_transition_clone(const rift_regex_transition_t *transition)
{
    if (!transition) {
        return NULL;
    }

    rift_regex_transition_t *clone;

    if (transition->is_epsilon) {
        clone = rift_transition_create_epsilon(transition->from_state, transition->to_state);
    } else {
        clone = rift_transition_create(transition->from_state, transition->to_state,
                                       transition->input_pattern);
    }

    if (!clone) {
        return NULL;
    }

    /* Copy priority */
    clone->priority = transition->priority;

    return clone;
}

