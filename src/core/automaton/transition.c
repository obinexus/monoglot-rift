/**
 * @brief Check if a transition matches an input string
 *
 * @param transition The transition to check
 * @param input The string to match against
 * @return true if the transition matches the string, false otherwise
 */
bool
rift_transition_matches(const rift_regex_transition_t *transition, const char *input)
{
    if (!transition || !input) {
        return false;
    }

    /* Epsilon transitions always match (without consuming input) */
    if (transition->is_epsilon) {
        return true;
    }

    /* We need a pattern to match against */
    if (!transition->input_pattern) {
        return false;
    }

    /* Compile the regex pattern */
    regex_t regex;
    int result = regcomp(&regex, transition->input_pattern, REG_EXTENDED);
    if (result != 0) {
        return false;
    }

    /* Check if the pattern matches at the start of the input */
    result = regexec(&regex, input, 0, NULL, 0);

    /* Free the compiled regex */
    regfree(&regex);

    /* Return true if the pattern matched */
    return (result == 0);
}

